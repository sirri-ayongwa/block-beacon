const LS_KEY = "bb.lang";
export const LANG_CHANGE_EVENT = "bb:langchange";

export function getStoredLang(): LangCode | null {
  if (typeof window === "undefined") return null;
  const v = window.localStorage.getItem(LS_KEY);
  return v && v in LANG_NAMES ? (v as LangCode) : null;
}

export function setStoredLang(l: LangCode) {
  if (typeof window === "undefined") return;
  window.localStorage.setItem(LS_KEY, l);
  window.dispatchEvent(new CustomEvent(LANG_CHANGE_EVENT, { detail: l }));
}

export function t(lang: LangCode, key: DictKey): string {
  return DICT[lang]?.[key] ?? EN[key] ?? String(key);
}

// RTL languages need to flip document direction
export const RTL_LANGS: LangCode[] = ["ar", "ur", "fa"];
export function isRtl(lang: LangCode): boolean {
  return RTL_LANGS.includes(lang);
}
